def test_q_shift_logic():
    """Verify that Q-Shift correctly shifts tokens in 4 directions."""
    B, H, W, C = 1, 4, 4, 16
    head_dim = 16
    # Create a tensor where each pixel has a unique value in its first channel
    # to track movement.
    x = torch.zeros(B, H * W, C)
    for i in range(H * W):
        x[0, i, 0] = i + 1

    # Q-Shift splits C into 4 groups of 4.
    # Group 0: Shift Right (W+)
    # Group 1: Shift Left (W-)
    # Group 2: Shift Down (H+)
    # Group 3: Shift Up (H-)

    out = q_shift_multihead(
        x, shift_pixel=1, head_dim=head_dim, patch_resolution=(H, W)
    )

    x_grid = x.view(B, H, W, C)
    out_grid = out.view(B, H, W, C)

    # Check Group 0 (channels 0-3): Shift Right
    # out[0, 0, 1, 0] should be x[0, 0, 0, 0]
    assert out_grid[0, 0, 1, 0] == x_grid[0, 0, 0, 0]
    assert out_grid[0, 0, 0, 0] == 0  # Padded with zero

    # Check Group 1 (channels 4-7): Shift Left
    # out[0, 0, 0, 4] should be x[0, 0, 1, 4]
    assert out_grid[0, 0, 0, 4] == x_grid[0, 0, 1, 4]

    # Check Group 2 (channels 8-11): Shift Down
    # out[0, 1, 0, 8] should be x[0, 0, 0, 8]
    assert out_grid[0, 1, 0, 8] == x_grid[0, 0, 0, 8]

    # Check Group 3 (channels 12-15): Shift Up
    # out[0, 0, 0, 12] should be x[0, 1, 0, 12]
    assert out_grid[0, 0, 0, 12] == x_grid[0, 1, 0, 12]


