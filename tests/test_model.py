def test_block_v_first_propagation():
    """Verify that v_first propagation affects block output."""
    n_embd = 64
    n_head = 1
    block = Vision_RWKV7_Block(n_embd=n_embd, n_head=n_head, n_layer=2, layer_id=1)

    x = torch.randn(1, 16, n_embd)
    res = (4, 4)

    # Case 1: No v_first (defaults to zero in _scan if not provided)
    out1, _, _ = block(x, res, v_first_fwd=None, v_first_bwd=None)

    # Case 2: With v_first
    vf_fwd = torch.randn(1, 16, n_embd)
    vf_bwd = torch.randn(1, 16, n_embd)
    out2, _, _ = block(x, res, v_first_fwd=vf_fwd, v_first_bwd=vf_bwd)

    # Outputs should be different
    assert not torch.allclose(out1, out2, atol=1e-5)


